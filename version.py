def get_version_info():
    """获取当前版本的完整信息"""
    for v in VERSION_HISTORY:
        if v["version"] == VERSION:
            return v
    # 如果没找到，返回第一个（最新的）
    if VERSION_HISTORY:
        return VERSION_HISTORY[0]
    return None

